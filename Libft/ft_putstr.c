#include "libft.h"

void	ft_putstr(char const *s)
{
	unsigned char *cs;

	if (s)
	{
		cs = (unsigned char*)s;
		while (*cs != '\0')
		{
			ft_putchar(*cs);
			cs++;
		}
	}
}
