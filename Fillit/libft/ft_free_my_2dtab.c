#include "libft.h"

void	*ft_free_my_2dtab(char **tab, int x)
{
	x--;
	if (tab)
	{
		while (x >= 0)
		{
			free(tab[x]);
			tab[x] = NULL;
			x--;
		}
	}
	return ((void*)NULL);
}
